## How to Use the Binder on the Wedding Day

Assembling the binder is half the job. Using it effectively is the other half.

Who Holds the Binder? The lead planner or coordinator keeps the master. A backup copy goes with an assistant or trusted family member — essential for DIY couples without a professional coordinator.

Briefing VIPs: Walk through the simplified one-page timeline at the rehearsal. Email or hand out abbreviated versions to the wedding party so everyone knows their role and timing without needing access to the full document.

Day-Of Rhythms: Check the binder at natural breaks: after hair and makeup, before ceremony line-up, before reception formalities. Confirm next steps against the wedding itinerary rather than relying on memory.

Logging Changes: Use quick handwritten notes or sticky flags for time shifts (e.g., toasts pushed from 7:35 to 7:45 p.m.). Small adjustments are inevitable — capture them so the whole team stays aligned.

End-of-Night Handoff: Review the end-of-night checklist before leaving the venue. Sign off with the venue manager and confirm all rented items, personal décor, and gifts have been accounted for.

