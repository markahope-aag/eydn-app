## Creating a B List

A B list can be a strategic tool in wedding planning, especially if you want to ensure your guest list is full without overcommitting early on. However, many wedding experts, including planner Dani Blasena, caution against relying too heavily on a B list, as it can sometimes lead to awkward situations or hurt feelings among friends and family.

If you do decide to create a B list, reserve it for special circumstances—such as new friends made after your initial invitations go out or unexpected changes in your guest count. To keep things smooth, send save-the-dates to your A list guests at least nine months before the wedding. Formal invitations should follow six to eight weeks before the big day, or eight to ten weeks for destination weddings.

Set RSVP deadlines two to three weeks after sending invitations. This gives you enough time to invite B list guests if spots open up, without making anyone feel like a last-minute addition. Thoughtful planning and clear communication are key to managing your guest list and B list gracefully.

