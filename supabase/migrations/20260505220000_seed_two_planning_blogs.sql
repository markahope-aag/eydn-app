<h2>9–12 months out</h2>
<ul>
<li>Book your caterer if not included with your venue. Ask for an itemized breakdown, not just a per-head package price. Delivery fees, setup charges, cake cutting fees, and overtime rates can add 15–20% to a quote that looked reasonable on the surface.</li>
<li>Book your band or DJ. Live bands book out faster than DJs in most markets. If you have a specific performer or band in mind, this is not one to delay.</li>
<li>Begin dress or attire shopping. Production and alteration lead times are typically 4–6 months minimum, sometimes longer for custom or designer pieces. Starting now gives you options. Starting at month 4 doesn''t.</li>
<li>Create your wedding website. Get the URL locked in before you send save-the-dates — changing it later creates confusion.</li>
<li>Send save-the-dates. Give guests as much runway as possible, especially if you have a lot of out-of-town attendees or if your date falls on a long weekend.</li>
<li>Book room blocks at nearby hotels for out-of-town guests. Negotiate a group rate, confirm the release date, and include the details on your wedding website.</li>
<li>Book your officiant. Licensed officiants with strong reputations fill up — this one surprises a lot of couples.</li>
<li>Start building your florist shortlist. Floral styles vary enormously between designers. Look at full wedding galleries, not just portfolio highlights.</li>
<li>Start centralizing all vendor information in one place — contact details, booking confirmation dates, payment schedules, and contract terms.</li>
</ul>

<h2>6–9 months out</h2>
<ul>
<li>Book your florist. Bring a rough budget, your venue details, and a few reference photos. Ask explicitly what''s achievable within your number before you fall in love with anything.</li>
<li>Book hair and makeup artists. If you want a trial run — which is worth doing — factor that into the timeline and cost.</li>
<li>Book any additional vendors — photo booth, transportation, lighting, draping, or anything else specific to your venue or vision.</li>
<li>Finalize your guest list. The number needs to be real and it needs to stop growing. Set the ceiling, communicate it to both families, and hold it. Every addition has a ripple effect on catering, invitations, seating, and favors.</li>
<li>Send formal invitations. Eight to ten weeks before the wedding is standard for local guests. Add another two to four weeks for destination weddings or guests traveling internationally.</li>
<li>Build your gift registry. Think across price points so guests at every budget have options. Include a mix of physical items and cash funds. Don''t leave the registry until the last minute — guests start buying gifts as soon as save-the-dates arrive.</li>
<li>Book your honeymoon. Flight and accommodation prices go up the longer you wait, and availability in popular destinations closes out months in advance.</li>
<li>Research and book your wedding cake or desserts. Schedule a tasting — most bakers offer them and they''re worth doing.</li>
<li>Schedule your first dress fitting. Alterations typically take 2–3 appointments over 6–8 weeks. Build that time in.</li>
<li>Plan your rehearsal dinner — decide on the format, guest list, venue, and catering. It doesn''t have to be elaborate, but it does need to be booked.</li>
<li>Decide on and order wedding party attire. Give your wedding party enough lead time that alterations aren''t a last-minute panic.</li>
</ul>

<h2>4–6 months out</h2>
