<p>You don''t need to do every item on this list. Skip the ones that don''t apply. Focus on the ones that do. The point is having the full map, not following it perfectly.</p>

<h2>12+ months out</h2>
<ul>
<li>Set your overall budget — the real number, not the aspirational one. What would you genuinely be comfortable spending if a practical friend asked you directly?</li>
<li>Build in a 10% contingency buffer before you allocate anything. Hidden costs, last-minute additions, and price increases are the norm, not the exception.</li>
<li>Have the guest list conversation with both families early. Agree on a ceiling before anyone starts adding names — it''s much harder to cut the list once expectations are set.</li>
<li>Draft your guest list in two tiers. Tier 1 is everyone you''d be genuinely upset to leave out. Tier 2 is everyone you''d invite if the venue allows. Know both numbers before you start venue shopping.</li>
<li>Agree on wedding priorities as a couple before you spend anything. Photography matters more than florals to some couples. Venue matters more than food to others. Get aligned before vendors start pitching you.</li>
<li>Research venues and visit your top choices. Ask about what''s included, what''s not, preferred vendor lists, and whether you''re required to use their caterer.</li>
<li>Book your venue — this is the single most time-sensitive decision. Peak season Saturdays in most markets are gone 12–18 months out. Everything else follows the venue date.</li>
<li>Set your wedding date once the venue is confirmed.</li>
<li>Start researching photographers immediately after booking your venue. The best photographers in most markets are already booked 12+ months ahead. Don''t leave this one for later.</li>
<li>Decide whether you want a full wedding planner, a month-of coordinator, or a day-of coordinator. These are meaningfully different services at meaningfully different price points.</li>
<li>Book your planner or coordinator if using one — good ones fill up as fast as photographers.</li>
