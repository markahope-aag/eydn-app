<li>Confirm vendor payments are with the right person before things get busy.</li>
<li>Let your point person handle any logistics that come up. If something goes wrong — and something small always does — you don''t need to know about it in the moment.</li>
<li>Take a moment alone with your partner before the ceremony if you can. Even five minutes. You''ll be glad you did.</li>
<li>Be present. The planning is done. This is the day itself.</li>
