<li>Start your thank you card address list now while you still have energy for it. Source addresses from your RSVP responses.</li>
<li>Confirm honeymoon check-in details. Print or save confirmation numbers, travel documents, and hotel addresses offline in case you don''t have signal.</li>
<li>Schedule any final beauty prep — haircut, facial, or anything else with a lead time — for the week before.</li>
