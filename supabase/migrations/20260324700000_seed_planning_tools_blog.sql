RSVP status

Dietary notes

Many wedding platforms connect guest list modules automatically to RSVP pages and seating charts. Filters help you view non-responders, compile follow-up lists, or pull counts for dietary meals. Back up guest lists offline periodically — losing that data close to the wedding day is a nightmare no one needs.

